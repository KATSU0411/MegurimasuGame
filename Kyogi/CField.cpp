// Fieldクラス
#include <stdlib.h>

#include"CField.h"

// --------------------------------------------------------------
// コンストラクタ
// --------------------------------------------------------------
Field::Field() {
	Init();
}

// --------------------------------------------------------------
// デストラクタ
// --------------------------------------------------------------
Field::~Field() {

}

// --------------------------------------------------------------
// 初期化処理
// --------------------------------------------------------------
int Field::Init() {
	for (int y = 0; y < YFIELD; y++) {
		for (int x = 0; x < XFIELD; x++) {
			m_Field[y][x].Point=rand()%33 - 16;
			m_Field[y][x].State = STATE_NONE; 
		}
	}

	m_MyPoint = 0;
	m_EnemyPoint = 0;
}

